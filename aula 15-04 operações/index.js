resultado = primeiroNumero !== segundoNumero // se o primeiro número for diferente de
alert(resultado)

resultado = primeiroNumero > segundoNumero // se primeiro número for maior que segundo número
alert(resultado)

resultado = primeiroNumero < segundoNumero // se primeiro número for menor que segundo número
alert(resultado) // mostra o resultado na tela

